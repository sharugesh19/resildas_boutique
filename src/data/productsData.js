export default products

export const getProductById       = (id)       => products.find((p) => p.id === id) ?? null
export const getProductsByCategory = (category) => products.filter((p) => p.category === category)
export const getFeaturedProducts  = ()          => products.filter((p) => p.isFeatured)
export const getNewArrivals       = ()          => products.filter((p) => p.isNewArrival)
export const getRelatedProducts   = (product, limit = 4) =>
  products.filter((p) => p.category === product.category && p.id !== product.id).slice(0, limit)
export const searchProducts = (query) => {
  const q = query.toLowerCase().trim()
  if (!q) return products
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.subcategory.toLowerCase().includes(q) ||
      p.fabric.toLowerCase().includes(q) ||
      p.occasion.some((o) => o.toLowerCase().includes(q))
  )
}