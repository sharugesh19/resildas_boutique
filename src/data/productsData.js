// ── Helper: get the "active" color variant data from a product ──────────────
// If product has colors[], use the selected one. Otherwise fall back to root.
export function getActiveVariant(product, colorName = null) {
  if (!product.colors?.length) return product
  const color = colorName
    ? product.colors.find((c) => c.name === colorName) ?? product.colors[0]
    : product.colors[0]
  return {
    ...product,
    price:         color.price         ?? product.price,
    originalPrice: color.originalPrice ?? product.originalPrice,
    images:        color.images        ?? product.images,
    inStock:       color.inStock       ?? product.inStock,
    activeColor:   color.name,
  }
}

